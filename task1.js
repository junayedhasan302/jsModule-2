// Take two variables price1 and price2, and compare them using all comparison operators (==, ===, !=, !==, >, <, >=, <=), printing each result with console.log.
// let price1 = Number(prompt("Enter first price:"));
// let price2 = Number(prompt("Enter second price:"));
let price1 = 100, price2 = 200;

console.log(price1 == price2);
console.log(price1 === price2);
console.log(price1 != price2);
console.log(price1 !== price2);
console.log(price1 > price2);
console.log(price1 < price2);
console.log(price1 >= price2);
console.log(price1 <= price2);

// == → Equal (value only)
// === → Strictly equal (value + data type)
// != → Not equal
// !== → Strictly not equal
// > → Greater than
// < → Less than
// >= → Greater than or equal
// <= → Less than or equal